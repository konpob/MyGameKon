  const messageContainer = document.getElementById('com1');

  // เพิ่มข้อความลงใน displayedMessages
  displayedMessages.push(message);

  // ตรวจสอบว่าเมื่อเพิ่มข้อความลงใน displayedMessages ทำให้ครบ 5 คำหรือไม่
